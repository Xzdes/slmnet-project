/**
 * @file slmnet/Loader.js
 * @description Модуль для асинхронной загрузки ресурсов (моделей, словарей, конфигов) по сети.
 * Использует нативный `fetch` и кэширует результаты, чтобы избежать повторных загрузок.
 */

// Простое внутримодульное кэширование, чтобы не загружать один и тот же файл дважды.
const cache = new Map();

/**
 * Асинхронно загружает и парсит JSON-файл по указанному URL.
 * Если файл уже был загружен, возвращает его из кэша.
 * @param {string} url - URL-адрес JSON-файла, который нужно загрузить.
 * @returns {Promise<object>} Промис, который разрешается объектом с распарсенными JSON-данными.
 * @throws {Error} Если сетевой запрос не удался или ответ не является валидным JSON.
 */
async function loadJson(url) {
    // 1. Проверяем, нет ли уже этого ресурса в кэше.
    if (cache.has(url)) {
        // console.log(`[Loader] Возврат из кэша: ${url}`);
        return cache.get(url);
    }

    // 2. Если в кэше нет, делаем сетевой запрос.
    // console.log(`[Loader] Загрузка: ${url}`);
    try {
        const response = await fetch(url);

        // 3. Проверяем, что запрос прошел успешно (статус 200-299).
        if (!response.ok) {
            throw new Error(`Сетевая ошибка: не удалось загрузить ${url}. Статус: ${response.status}`);
        }

        // 4. Парсим тело ответа как JSON.
        const data = await response.json();

        // 5. Сохраняем результат в кэш.
        cache.set(url, data);

        // 6. Возвращаем данные.
        return data;

    } catch (error) {
        console.error(`[Loader] Ошибка при загрузке или парсинге файла ${url}:`, error);
        // Пробрасываем ошибку дальше, чтобы ее можно было обработать на верхнем уровне.
        throw error;
    }
}

